#funcion para obtner los paises con su codigo desde una pagina web
def obtener_paises_codigo(url):
    tablaASCII =pd.read_html(url)[0]
    df=pd.DataFrame(tablaASCII)
    df_pais=df.iloc[:,0:3]
    df_pais.rename(columns={'Country': 'country_name', 'Alpha-3 code':'country_code'}, inplace='True')
    df_pais['country_name']=df_pais['country_name'].str.upper()
    df_pais.drop(['Alpha-2 code'],axis=1, inplace=True)
    return df_pais     


